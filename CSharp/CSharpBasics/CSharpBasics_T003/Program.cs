using System;

class Program
{
    static void Main()
    {
        // Boolean Data Type
        bool b = true;
        // Integral Data Types (sbyte, bite, short, ushort, int, uint, long, ulong, char)
        int i = 120;
        // int somethingWrong = 9999999999999999999999999;
        // Float ( 7 digit precision) or Double (~19 digit precision) or Decimal (  ) Data Type
        float f = 1.0f;
        double d = 1.0f;
        // String Data Type
        string s = "CSharpBasics_T3";

        Console.WriteLine("Bool " + b);
        Console.WriteLine("Int " + i);
        Console.WriteLine("Float " + f);
        Console.WriteLine("Double " + d);
        Console.WriteLine("String " + s);
        Console.WriteLine("Int Max Value " + int.MaxValue);
    }
}

